class Logic
{
    void SumofDigit(int iNo)
    {
        int iSum = 0, iRem = 0;

        while(iNo>0)
        {
            iRem = iNo % 10;

            iSum = iSum + iRem;

            iNo = iNo / 10;
        }

        System.out.println(+iSum);
    }
}

// Time Complexity = O(n)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////


class Program1
{
    public static void main(String A[])
    {
        Logic obj = new Logic();

        obj.SumofDigit(1234);
    }
}

